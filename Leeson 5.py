# На вход подается число N, затем N целых чисел, каждое число вводится с новой строки и сохраняется в список. 
# Заполните список числами, которые оканчиваются на 2 или 7. Отсортируйте список по возрастанию. 
# И выведете сумму индексов чисел, которые кратны 3. Гарантируется, что числа кратные 3, оканчивающиеся на 2 или 7, будут введены.

N = int(input())
a = []
s = 0
for i in range(N):
    x = int(input())
    if x % 10 == 2 or x % 10 == 7:
        a.append(x) 

a.sort()

for i in range(len(a)):
    if a[i] % 3 == 0:
        s += i
