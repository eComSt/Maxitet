# Пара элементов

# На вход программе с клавиатуры подается сначала число N, 
# затем N натуральных чисел, каждое число вводится с новой строки и сохраняется в список. 
# Определите и выведите на экран количество пар соседних элементов списка, в которых хотя бы один элемент чётный. 
N = int(input())
k = 0
a = []
for i in range(N):
    x = int(input())
    a.append(x) 

for i in range(N - 1):
    if a[i] % 2 == 0 or a[i + 1] % 2 == 0:
         k += 1
print(k)

