# Выведите среднее арифметическое чисел полученного списка.

p = 1
a = []
N = int(input())
for i in range(N):
    x = int(input())
    a.append(x) 
    if x % 4 == 0:
        p *= x

for i in range(N):
    if a[i] % 4 == 0:
        a[i] = p
