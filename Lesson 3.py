# Напишите программу, которая выводит на экран таблицу степеней двойки (от нулевой до десятичной).
for i in range(11):
    print('2 в', i, 'степени:', 2**i)

# Напишите программу, которая считает количество чисел 
# в диапазоне от 1000 до 10000 включительно, кратных 4 и не кратных 5.
k = 0
for i in range(1000, 10001):
    if i % 4 == 0 and i % 5 != 0:
        k += 1
print(k)

# Программа принимает на вход натуральное число n и вычисляет факториал этого числа.
n = int(input())
p = 1
for i in range(1, n + 1):
    p *= i
print(p)

# На вход программе подается натуральное число n, затем n целых чисел, каждое с новой строки. 
# Программа должна посчитать среднее арифметическое введенных чисел.
s = 0
n = int(input())
for i in range(n):
    x = int(input())
    s += x
print(s / n)

