# Циклы while, for

# Цикл while

# Написать программу, которая выводит первые 20 натуральных чисел. 
number = 1
while number <= 20:
    print(number)
    number += 1

# На вход программе подается число n. Напишите программу, которая выводит сумму первых n натуральных чисел.
n = int(input)
s = 0
while n > 0:
    s = s + n
    n = n - 1
print(s)

# На вход подается число a. Напишите программу, которая уменьшает это число на 8, 
# пока это число нечетное или больше 100, затем выводит полученное число на экран.
a = int(input())
while a % 2 != 0 or a > 100:
    a = a - 8
print(a)

