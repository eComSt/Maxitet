# Операторы break и continue в цикле while

# Программа с выводом квадратных корней, пока не будет введено слово 'стоп'
n = input()
while n != 'стоп':
    n = int(n)
    if n > 0:
        print(n ** 0.5)
    else:
        print('Число отрицательное')
        break
    n = input()
    print('Программа завершена')

# Напишите программу, которая выведет первые 10 натуральных чисел, кроме 8
number = 0
while number < 10:
    number = number + 1
    if number == 8:
        continue
    print(number)

